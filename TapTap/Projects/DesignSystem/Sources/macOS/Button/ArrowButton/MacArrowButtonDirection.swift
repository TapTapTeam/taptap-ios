/// 화살표 버튼의 방향을 정의하는 enum입니다.
///
/// `MacArrowButton`에서 사용되며,
/// 버튼의 아이콘과 모서리 형태를 결정합니다.
///
/// - back: 좌측으로 이동하는 버튼 (좌측 모서리가 둥근 형태)
/// - forward: 우측으로 이동하는 버튼 (우측 모서리가 둥근 형태)
public enum MacArrowButtonDirection {
  case back
  case forward
}
